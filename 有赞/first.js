// lang 可选一般不写， 选择脚本语言类型
//
// setInterval()按照指定周期来调用函数或者计算表达式。方法会不停地调用函数，直到clearInterval调用或者窗口关闭
// 传入三个参数
// code 必需 要调用的函数或者执行的代码串
// millisec 必需 周期性执行或者调用code之间的时间间隔
// lang 可选，一半不填 选择语言类型