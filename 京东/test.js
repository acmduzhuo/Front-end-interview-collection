//CSS Sprites 国内称为CSS精灵，是一种网页图片应用处理方式
// 原理：讲一个页面所涉及到的零星图片全都包含在一张大图中
// 当访问该页面时，载入的图片就不会像以前一样一张一张显示出来
// 优点：减少了http请求，最大的优点，减少图片的字节，几张小图片合为一张大图片的字节要小
// 缺点：合并的时候需要精密计算，留下的空间太宽有缝隙，太小有断裂，开发维护很难

